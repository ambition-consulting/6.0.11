package com.liferay.portalweb.portal.controlpanel.users.user.adduserdefaultuserassociationsrole;

import com.liferay.portalweb.portal.BaseTestCase;
import com.liferay.portalweb.portal.util.RuntimeVariables;

/**
 * @author Brian Wing Shun Chan
 */
public class EnterDefaultUserAssociationsRoleTest extends BaseTestCase {
	public void testEnterDefaultUserAssociationsRole()
		throws Exception {
		selenium.open("/web/guest/home/");

		for (int second = 0;; second++) {
			if (second >= 60) {
				fail("timeout");
			}

			try {
				if (selenium.isElementPresent("link=Control Panel")) {
					break;
				}
			}
			catch (Exception e) {
			}

			Thread.sleep(1000);
		}

		selenium.saveScreenShotAndSource();
		selenium.clickAt("link=Control Panel", RuntimeVariables.replace(""));
		selenium.waitForPageToLoad("30000");
		selenium.saveScreenShotAndSource();
		selenium.clickAt("link=Settings", RuntimeVariables.replace(""));
		selenium.waitForPageToLoad("30000");
		selenium.saveScreenShotAndSource();
		selenium.clickAt("usersLink", RuntimeVariables.replace("Users"));
		selenium.clickAt("link=Default User Associations",
			RuntimeVariables.replace(""));
		selenium.type("_130_settings--admin.default.role.names--",
			RuntimeVariables.replace("Administrator"));
		selenium.saveScreenShotAndSource();
		selenium.clickAt("//input[@value='Save']", RuntimeVariables.replace(""));
		selenium.waitForPageToLoad("30000");
		selenium.saveScreenShotAndSource();
		assertEquals(RuntimeVariables.replace(
				"Your request processed successfully."),
			selenium.getText("//section/div/div/div/div[1]"));
		selenium.clickAt("link=Default User Associations",
			RuntimeVariables.replace(""));
		assertEquals("Administrator",
			selenium.getValue("_130_settings--admin.default.role.names--"));
	}
}